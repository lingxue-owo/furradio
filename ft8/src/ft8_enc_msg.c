static void sybmap(u08* symbol, int pos)
{
	int i, j, n, b, t;
	const static u08 gray_table[8] = {0,1,3,2,5,6,4,7};
	for (i = 0 ; i < 29; ++i) {
		t = 0;
		for (j = 0; j < 3; j++) {
			n = 3 * i + j + pos;
			b = g_buff[n / 8] & (0x80u >> (n % 8));
			t |= b ? (0x4u >> j) : 0;
		}
		symbol[i] = gray_table[t];
	}
	return;
}

static void costas(u08* symbol)
{
	int i;
	static u08 costas[7] = {3,1,4,0,6,5,2};
	for (i = 0; i < 7; i++)
		symbol[i] = costas[i];
	return;
}

