static void data_to_t71(const u08 *data)
{
	int i, j, cnt;
	g_buff[0] |= (data[0] & 0x07) << 5;
	cnt = 3;
	for (i = 1; i < 18; i++) {
		for (j = 0; j < 4; j++) {
			if (data[i] & (0x08 >> (j % 4)))
				g_buff[cnt/8] |= 0x80 >> (cnt % 8);
			++cnt;
		}
	}
}

